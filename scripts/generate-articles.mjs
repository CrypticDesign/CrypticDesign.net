// CRY-344 item 3: turns scripts/articles.raw.json into src/lib/articles.ts
// and downloads hero images into public/images/articles/.
import { readFile, writeFile } from "node:fs/promises";
import { createWriteStream } from "node:fs";
import { pipeline } from "node:stream/promises";
import { Readable } from "node:stream";
import path from "node:path";

const raw = JSON.parse(await readFile("scripts/articles.raw.json", "utf8"));
const IMG_DIR = path.join("public", "images", "articles");

async function grabHero(slug, url) {
  if (!url) return "";
  const ext = (url.match(/\.(png|jpe?g|webp)$/i)?.[1] || "png").toLowerCase();
  const file = `${slug}.${ext}`;
  const dest = path.join(IMG_DIR, file);
  const res = await fetch(`${url}?format=1500w`, {
    headers: { "User-Agent": "Mozilla/5.0" },
  });
  if (!res.ok) {
    console.log(`  hero FAIL ${slug} (${res.status})`);
    return "";
  }
  await pipeline(Readable.fromWeb(res.body), createWriteStream(dest));
  return `/images/articles/${file}`;
}

const articles = [];
for (const a of raw) {
  const hero = await grabHero(a.slug, a.heroRemote);
  articles.push({
    slug: a.slug,
    title: a.title,
    description: a.description,
    published: a.published ? a.published.slice(0, 10) : "",
    categories: a.categories,
    tags: a.tags,
    hero,
    blocks: a.blocks,
  });
  console.log(`OK ${a.slug} -> ${hero || "(no hero)"}`);
}

// Newest first; undated entries sort last.
articles.sort((x, y) => (y.published || "").localeCompare(x.published || ""));

const header = `// GENERATED by scripts/generate-articles.mjs — CRY-344 article migration.
// Source: legacy crypticdesign.net Squarespace posts (owned content).
// Re-run the importer + generator to refresh; do not hand-edit block text.

export type ArticleBlock =
  | { type: "paragraph" | "h2" | "h3" | "h4" | "quote"; text: string }
  | { type: "list" | "orderedList"; items: string[] }
  | { type: "divider" };

export type Article = {
  slug: string;
  title: string;
  description: string;
  published: string;
  categories: string[];
  tags: string[];
  hero: string;
  blocks: ArticleBlock[];
};

export const articles: Article[] = `;

const footer = `;

export function allArticles(): Article[] {
  return articles;
}

export function getArticle(slug: string): Article | undefined {
  return articles.find((a) => a.slug === slug);
}
`;

await writeFile(
  "src/lib/articles.ts",
  header + JSON.stringify(articles, null, 2) + footer,
  "utf8",
);
console.log(`\nWrote src/lib/articles.ts (${articles.length} articles)`);
